#include "../../include/libft.h"

int	ft_memcmp(const void *ptr1, const void *ptr2, size_t size)
{
	const char	*p1;
	const char	*p2;

	p1 = ptr1;
	p2 = ptr2;
	while (size--)
	{
		if (*p1 != *p2)
			return ((unsigned char)*p1 - (unsigned char)*p2);
		p1++;
		p2++;
	}
	return (0);
}
